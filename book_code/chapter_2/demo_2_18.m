% 已知零点极点和增益求传递函数
format long % 显示更多小数位数

zr = [0.21 3.14 -0.3+0.5j -0.3-0.5j]; % 零极点行向量
pr = [-0.45 0.67 0.81+0.72j 0.81-0.72j]; % 极点行向量
k = 2.2; % 增益

% 转置操作
z = zr'; p = pr';

% 零极点增益（ZPK）模型转换为传递函数（TF)
[num, den] = zp2tf(z, p, k);

% 输出结果
disp("num:");disp(num);
disp("den:");disp(den);












